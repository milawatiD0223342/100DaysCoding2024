package Hari051;

public class Day51 {

    public static void main(String[] args) {
        char inisial = 'M';
        switch (inisial) {
            case 'S':
                System.out.println("Inisial S");
                break;
            case 'L':
                System.out.println("Inisial L");
                break;
            case 'M':
                System.out.println("Inisial M");
                break;
            default:
                System.out.println("Inisial benar");
        }
    }
}
