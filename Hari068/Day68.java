package Hari068;

public class Day68 {

    public static int jumlah(int a, int b) {
        int hasil = a + b;
        return hasil;
    }

    public static void main(String[] args) {
        int c = 2;
        int d = 3;
        int hasilJumlah = jumlah(c, d);
        System.out.println(hasilJumlah);
    }
}
