public class Day82 {

    public static void main(String[] args) {
        double a = 3.5;
        double ceil = Math.ceil(a);
        System.out.println("ceil : " + ceil);
        double floor = Math.floor(a);
        System.out.println("floor : " + floor);
        long round = Math.round(a);
        System.out.println("round : " + round);
    }
}
